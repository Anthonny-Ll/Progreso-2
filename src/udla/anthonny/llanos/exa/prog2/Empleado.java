package udla.anthonny.llanos.exa.prog2;

abstract public class Empleado {
    private String id;
    private String nombre;
    private double salario;

    public Empleado() {
    }

    //Metodos

    //Getters
    public String getId() {
        return id;
    }


    public String getNombre() {
        return nombre;
    }


    public double getSalario() {
        return salario;
    }

}
